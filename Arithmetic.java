public class Arithmetic {
    public static void main(String[] args) {
        int a = 15, b = 10;
        System.out.println("Arithemetic Operators");
        System.out.println("a + b = " + (a + b));

        System.out.println("a - b = " + (a - b));

        System.out.println("a * b = " + (a * b));

        System.out.println("a / b = " + (a / b));

        System.out.println("a % b = " + (a % b));
    }

}
